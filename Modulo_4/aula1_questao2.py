# Leitura de dados (entrada)
n = int(input("Digite um número: "))

# Inicializar as variáveis
cont = 0 # variável de controle do loop

# Iterações
while n < cont:
    
    # atualizando a variável de controle do loop
    cont += 1 # cont = cont + 1
    print(cont)

# Impressão de dados (saída)
print("Fim!")